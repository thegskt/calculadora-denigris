function parseNumeroBR(str){
  if(!str) return NaN;
  // Remove R$, espaos, pontos milhar e troca vrgula por ponto
  return parseFloat(str.replace(/R\$\s*/i,'').replace(/\./g,'').replace(/,/g,'.'));
}

async function garantirPrecosAno(ano){
  if(!ano || fabPrecosPorAno[ano]) return;
  const url = FAB_PRECO_URLS[ano];
  if(!url) return;
  try{
    const res = await fetch(url,{cache:'no-store'});
    const csv = await res.text();
    fabPrecosPorAno[ano] = parseFabPrecoCSV(csv);
  }catch(e){
    console.warn("Falha ao carregar preos fbrica ano", ano, e);
    fabPrecosPorAno[ano] = {};
  }
}

function parseFabPrecoCSV(csv){
  const lines = csv.split(/\r?\n/).filter(l=>l.trim());
  if(!lines.length) return {};
  const splitSmart = line => {
    // separa por vrgula ignorando vrgulas entre aspas
    return line.split(/,(?=(?:(?:[^"]*"){2})*[^"]*$)/).map(c=>c.replace(/^"|"$/g,'').trim());
  };
   // Mantemos header s para referncia (no dependemos dele para as colunas de ao)
  const header = splitSmart(lines.shift());
    // Mapeamento fixo informado: (letras -> ndices 0-based)
  // I=9  K=11  M=13  O=15  Q=17
  const IDX = {
    CHAVE: 0,        // A
    TABELA: 4,       // E (coluna "TABELA")
    ESTOQUE: 8,      // I
    POSTOS: 10,      // K
    FRIGOR: 12,      // M
    CEABAST: 14,     // O
    MAISAL: 16       // Q
  };
  const idxChave  = header.indexOf("CHAVE");
  const idxTabela = header.indexOf("TABELA");
  const map = {};
  lines.forEach(l=>{
    const cols = splitSmart(l);
    const chave = (cols[IDX.CHAVE]||'').trim();
    if(!chave) return;

    const tabelaNum = parseNumeroBR(cols[IDX.TABELA]||'');
    if(isNaN(tabelaNum)) return; // sem tabela ignora

    function num(idx){
      const v = parseNumeroBR(cols[idx]||'');
      return isNaN(v)? null : v;
    }

    map[chave.toUpperCase()] = {
      tabela: tabelaNum,
      precos: {
        "Estoque": num(IDX.ESTOQUE),
        "Postos de Combustiveis": num(IDX.POSTOS),
        "Frigorificado": num(IDX.FRIGOR),
        "C.E.ABAST": num(IDX.CEABAST),
        "Mais Alimentos": num(IDX.MAISAL)
      },
      _raw: cols
    };
  });
  return map;
}
